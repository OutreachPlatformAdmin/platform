	<h2 class="w-full mt-5 pt-3 pb-5 text-4xl border-t-4 border-b-4 border-slate-500 text-center">
		The Full Story
	</h2>
	<p class="indent-10 my-5">Some of the biggest problems in the world today are:</p>
	<ul class="grid grid-cols-2 text-center">
		<li class="list-none">low wages</li>
		<li class="list-none">poverty</li>
		<li class="list-none">homelessness</li>
		<li class="list-none">unaffordable housing</li>
		<li class="list-none">food insecurity</li>
		<li class="list-none">never-ending wars</li>
		<li class="list-none">crumbling infrustructure</li>
		<li class="list-none">lack of health care</li>
		<li class="list-none">job insecurity</li>
		<li class="list-none">worsenning workplace conditions</li>
		<li class="list-none">climate change</li>
		<li class="list-none">and more</li>
	</ul>
	<p class="indent-10 my-5">
		Capitalism is an economic system that is based on private ownership of the means of production
		(one or a few owners decide what the company does with their production ability and with the
		money that is made from that business, not the workers or society as a whole) and the creation
		of goods and services for profit. While it has been successful in driving innovation and
		economic growth, it has also been responsible for some of the biggest problems in the world
		today.
	</p>
	<p class="indent-10 my-5">
		One of the biggest problems is income inequality. Capitalism tends to concentrate wealth in the
		hands of a small percentage of individuals, leaving many people struggling to make ends meet.
		This can result in a lack of access to basic necessities like housing, healthcare, and
		education.
	</p>

	<p class="indent-10 my-5">
		Capitalism also contributes to environmental degradation. The drive for profit often leads to
		the exploitation of natural resources, which can have serious consequences for the planet, such
		as climate change, deforestation, and pollution.
	</p>

	<p class="indent-10 my-5">
		Another issue with capitalism is its tendency to prioritize short-term gains over long-term
		sustainability. This can lead to a focus on maximizing profits at the expense of workers'
		rights, public safety, and the health of the environment.
	</p>

	<p class="indent-10 my-5">
		Finally, capitalism is also responsible for creating a culture of consumerism and materialism,
		where people are encouraged to constantly buy and consume more products, often leading to a
		sense of dissatisfaction and alienation.
	</p>

	<p class="indent-10 my-5">
		It's important to note that capitalism is not inherently evil, but rather it has some
		fundamental flaws that can lead to negative consequences if left unchecked. To address these
		issues, there are various proposed solutions, such as increased regulation, progressive
		taxation, and alternative economic models such as socialism or mixed economies.
	</p>
</section>

